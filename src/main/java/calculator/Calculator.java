package calculator;

public class Calculator {
	
	public void addition(int f,int s) {
		System.out.println("addition is " + (f+s));
	}
	
	public void subtraction(int f,int s) {
		System.out.println("subtraction is " + (f-s));
	}
	
	public void multiply(int f,int s) {
		System.out.println("multiply is " + (f*s));
	}
	
	public void division(int f,int s) {
		System.out.println("division is " + (f/s));
	}

	

}
